import mongoose, { mongo } from "mongoose";

const imageSchema = new mongoose.Schema({
    url: {
        type: String,
        required: true
    }
})
const productSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true,
        min: 0
    },
    stock: {
        type: Number,
        required: true,
        min: 0,
        default: 0
    },
    category: {
        type: String,
        required: true
    },
    images: [imageSchema],
    averageRating: {
        type: Number,
        min: 0,
        max: 5,
        default: 0
    },
    totalReviews: {
        type: Number,
        default: 0
    }
},
    { timestamps: true });

export const Product = mongoose.model("Product", productSchema);